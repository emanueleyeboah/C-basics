#include <stdio.h>


// This algorithm teaches how to use increment and decrement

int main ()
{
	int i = 10;

	i ++ ;

	// this is the same as i = i + 1
	
	printf ("\n%d\n", i);

	float k = 23.7;

	k ++ ;
        
	// same as k = k + 1
	printf("\n%f\n",k);

	char m = 'C';
         
	// m = m - 1
	m --;

	printf("\n%c\n",m);


}
